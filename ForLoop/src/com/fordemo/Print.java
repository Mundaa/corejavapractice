package com.fordemo
;
//declaring a class
public class Print {

	//main started
	public static void main(String[] args) {

		// for loop to print 1 to 10
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}

		// for loop to print horizontally
		for (int i = 1; i <= 5; i++) {
			System.out.print(i + "  ");
		}//end of for loop
	}//end of main

}//end of class
